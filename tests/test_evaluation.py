# 3. Reconstruction accuracy: better than naive baselines
# -----------------------------------------------------------------------

class TestReconstructionAccuracy:
    """Spectral reconstruction should beat simple baselines."""

    def test_beats_constant_baseline(self, predictor, speclib):
        """Model reconstruction MSE should be lower than predicting the
        per-wavelength mean radiance across the test set."""
        rng = np.random.default_rng(88)
        sensor = SENTINEL2_MSI
        n_test = 30

        true_spectra = []
        pred_spectra = []

        for spec in speclib.spectra[:n_test]:
            band_rad, true_rad, _ = _make_test_case(spec, WL_DENSE, sensor, rng=rng)
            pred = predictor.predict(
                wavelength_nm=sensor.center_wavelength_nm,
                fwhm_nm=sensor.fwhm_nm,
                radiance=band_rad,
                query_wavelength_nm=QUERY_WL,
                n_samples=16,
            )
            true_at_query = np.interp(QUERY_WL, WL_DENSE, true_rad)
            true_spectra.append(true_at_query)
            pred_spectra.append(pred.spectral_mean)

        true_arr = np.stack(true_spectra)   # (N, Q)
        pred_arr = np.stack(pred_spectra)   # (N, Q)

        model_mse = np.mean((true_arr - pred_arr) ** 2)
        # Constant baseline: predict the mean spectrum.
        mean_spectrum = true_arr.mean(axis=0)
        baseline_mse = np.mean((true_arr - mean_spectrum) ** 2)

        assert model_mse < baseline_mse, (
            f"Model MSE ({model_mse:.4f}) not better than constant baseline ({baseline_mse:.4f})"
        )

    def test_beats_linear_interpolation(self, predictor, speclib):
        """Model should beat linear interpolation between observed band values."""
        rng = np.random.default_rng(99)
        sensor = LANDSAT8_OLI  # Only 7 bands — interpolation is a weak baseline here.
        n_test = 25

        model_errors = []
        interp_errors = []

        for spec in speclib.spectra[:n_test]:
            band_rad, true_rad, _ = _make_test_case(spec, WL_DENSE, sensor, rng=rng)
            true_at_query = np.interp(QUERY_WL, WL_DENSE, true_rad)

            # Model prediction.
            pred = predictor.predict(
                wavelength_nm=sensor.center_wavelength_nm,
                fwhm_nm=sensor.fwhm_nm,
                radiance=band_rad,
                query_wavelength_nm=QUERY_WL,
                n_samples=16,
            )
            model_errors.append(np.mean((true_at_query - pred.spectral_mean) ** 2))

            # Linear interpolation baseline.
            interp_pred = np.interp(QUERY_WL, sensor.center_wavelength_nm, band_rad)
            interp_errors.append(np.mean((true_at_query - interp_pred) ** 2))

        model_mse = np.mean(model_errors)
        interp_mse = np.mean(interp_errors)

        assert model_mse < interp_mse, (
            f"Model MSE ({model_mse:.4f}) not better than linear interp ({interp_mse:.4f})"
        )

    def test_more_bands_lower_error(self, predictor, speclib):
        """Reconstruction error should decrease as band count increases."""
        rng = np.random.default_rng(111)
        band_counts = [5, 20, 100]
        mean_mses = []

        for n_bands in band_counts:
            mses = []
            for spec in speclib.spectra[:15]:
                refl = np.clip(np.nan_to_num(spec.resample(WL_DENSE)), 0, 1)
                rtm = simplified_toa_radiance(refl, WL_DENSE)
                sensor = sample_virtual_sensor(rng, n_bands_range=(n_bands, n_bands), strategy="regular")
                band_rad = apply_sensor(sensor, WL_DENSE, rtm.toa_radiance).astype(np.float32)

                pred = predictor.predict(
                    wavelength_nm=sensor.center_wavelength_nm,
                    fwhm_nm=sensor.fwhm_nm,
                    radiance=band_rad,
                    query_wavelength_nm=QUERY_WL,
                    n_samples=16,
                )
                true_at_query = np.interp(QUERY_WL, WL_DENSE, rtm.toa_radiance)
                mses.append(np.mean((true_at_query - pred.spectral_mean) ** 2))
            mean_mses.append(np.mean(mses))

        for i in range(len(band_counts) - 1):
            assert mean_mses[i] > mean_mses[i + 1], (
                f"MSE did not decrease: {band_counts[i]} bands ({mean_mses[i]:.4f}) "
                f"vs {band_counts[i+1]} bands ({mean_mses[i+1]:.4f})"
            )


