from spectralnp.model.spectralnp import SpectralNP, SpectralNPConfig

MODEL_PATH = Path(__file__).parent.parent / "demo_model.pt"
WL_DENSE = np.arange(380.0, 2501.0, 5.0)
QUERY_WL = np.arange(400.0, 2451.0, 5.0)


@pytest.fixture(scope="module")
def trained_model():
    """Load the trained checkpoint once for all tests in this module."""
    if not MODEL_PATH.exists():
        pytest.skip("demo_model.pt not found — run train_demo.py first")
    ckpt = torch.load(MODEL_PATH, map_location="cpu", weights_only=False)
    model = SpectralNP(ckpt["config"])
    model.load_state_dict(ckpt["model_state_dict"])
    model.eval()
    return model


@pytest.fixture(scope="module")
def predictor(trained_model):
    return SpectralNPPredictor(trained_model)


@pytest.fixture(scope="module")
def speclib():
    """Generate a held-out test library (different seed from training)."""
    return generate_synthetic_library(n_per_class=20, seed=9999)


def _make_test_case(spec, wl_dense, sensor, atmos=None, geom=None, rng=None):
    """Helper: produce (band_radiance, true_dense_radiance, atmos) for a test case."""
    if atmos is None:
        atmos = AtmosphericState(aod_550=0.2, water_vapour=1.8)
    if geom is None:
        geom = ViewGeometry(solar_zenith_deg=35)
    refl = np.clip(np.nan_to_num(spec.resample(wl_dense)), 0, 1)
    rtm = simplified_toa_radiance(refl, wl_dense, atmos=atmos, geometry=geom)
    band_rad = sensor.convolve(wl_dense, rtm.toa_radiance).astype(np.float32)
    if rng is not None:
        band_rad = add_sensor_noise(band_rad, rng, snr_range=(200, 200)).astype(np.float32)
    return band_rad, rtm.toa_radiance, atmos


