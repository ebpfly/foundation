class TestMaterialDiscrimination:
    """Material classification should be above chance across material classes."""

    def test_correct_category_above_chance(self, predictor, speclib, trained_model):
        """The predicted probability for the true material category should
        exceed the 1/5 = 20% random baseline on average.

        The training speclib has n_per_class=60, so class indices [0..59] are
        minerals, [60..119] vegetation, etc.  We sum the probabilities for
        all indices belonging to the correct category.
        """
        rng = np.random.default_rng(44)
        sensor = AVIRIS_NG

        # Training layout: 60 per class (from train_demo.py)
        ckpt = torch.load(MODEL_PATH, map_location="cpu", weights_only=False)
        n_training_spectra = ckpt["speclib_size"]
        n_classes = 5
        n_per_class_train = n_training_spectra // n_classes
        cat_names = ["minerals", "vegetation", "soils", "water", "manmade"]

        correct_probs = []
        for cat_idx, cat in enumerate(cat_names):
            subset = speclib.filter_category(cat)
            for spec in subset.spectra[:10]:
                band_rad, _, _ = _make_test_case(spec, WL_DENSE, sensor, rng=rng)
                pred = predictor.predict(
                    wavelength_nm=sensor.center_wavelength_nm,
                    fwhm_nm=sensor.fwhm_nm,
                    radiance=band_rad,
                    n_samples=16,
                )
                # Sum probs for all training indices in this category.
                probs = pred.material_probs
                cat_start = cat_idx * n_per_class_train
                cat_end = (cat_idx + 1) * n_per_class_train
                cat_prob = probs[cat_start:min(cat_end, len(probs))].sum()
                correct_probs.append(cat_prob)

        mean_correct = np.mean(correct_probs)
        chance = 1.0 / n_classes
        assert mean_correct > chance, (
            f"Mean probability for correct category ({mean_correct:.3f}) "
            f"not above chance ({chance:.3f})"
        )

    def test_different_materials_get_different_predictions(self, predictor, speclib):
        """Vegetation and water should produce distinguishably different
        material probability distributions."""
        sensor = SENTINEL2_MSI

        veg_probs = []
        water_probs = []
        for spec in speclib.filter_category("vegetation").spectra[:10]:
            band_rad, _, _ = _make_test_case(spec, WL_DENSE, sensor)
            pred = predictor.predict(
                wavelength_nm=sensor.center_wavelength_nm,
                fwhm_nm=sensor.fwhm_nm,
                radiance=band_rad,
                n_samples=8,
            )
            veg_probs.append(pred.material_probs)

        for spec in speclib.filter_category("water").spectra[:10]:
            band_rad, _, _ = _make_test_case(spec, WL_DENSE, sensor)
            pred = predictor.predict(
                wavelength_nm=sensor.center_wavelength_nm,
                fwhm_nm=sensor.fwhm_nm,
                radiance=band_rad,
                n_samples=8,
            )
            water_probs.append(pred.material_probs)

        # Mean probability vectors should differ significantly.
        veg_mean = np.mean(veg_probs, axis=0)
        water_mean = np.mean(water_probs, axis=0)
        top_veg = np.argmax(veg_mean)
        top_water = np.argmax(water_mean)

        assert top_veg != top_water, (
            "Vegetation and water predict the same top class — "
            "model is not discriminating materials"
        )


