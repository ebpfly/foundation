# 2. Uncertainty scaling: fewer bands → wider uncertainty
# -----------------------------------------------------------------------

class TestUncertaintyScaling:
    """More spectral information should produce tighter predictions."""

    def test_fewer_bands_higher_uncertainty(self, predictor, speclib):
        """Mean predictive uncertainty should be clearly higher with very few
        bands (3) than with many (100).  We also check the overall trend using
        Spearman rank correlation rather than strict step-by-step monotonicity,
        since neighbouring band counts (e.g., 30 vs 50) can be noisy."""
        from scipy.stats import spearmanr

        rng = np.random.default_rng(55)
        band_counts = [3, 7, 30, 100]
        mean_stds = []

        for n_bands in band_counts:
            stds = []
            for spec in speclib.spectra[:15]:
                refl = np.clip(np.nan_to_num(spec.resample(WL_DENSE)), 0, 1)
                rtm = simplified_toa_radiance(refl, WL_DENSE)
                sensor = sample_virtual_sensor(rng, n_bands_range=(n_bands, n_bands), strategy="regular")
                band_rad = apply_sensor(sensor, WL_DENSE, rtm.toa_radiance).astype(np.float32)

                pred = predictor.predict(
                    wavelength_nm=sensor.center_wavelength_nm,
                    fwhm_nm=sensor.fwhm_nm,
                    radiance=band_rad,
                    query_wavelength_nm=QUERY_WL,
                    n_samples=32,
                )
                stds.append(pred.spectral_std.mean())
            mean_stds.append(np.mean(stds))

        # Endpoint test: 3 bands must have clearly higher uncertainty than 100.
        assert mean_stds[0] > mean_stds[-1], (
            f"3-band uncertainty ({mean_stds[0]:.4f}) not higher than "
            f"100-band ({mean_stds[-1]:.4f})"
        )

        # Trend test: negative rank correlation between band count and uncertainty.
        corr, _ = spearmanr(band_counts, mean_stds)
        assert corr < 0, (
            f"Uncertainty does not trend downward with more bands: "
            f"Spearman ρ={corr:.3f}, stds={[f'{s:.4f}' for s in mean_stds]}"
        )

    def test_latent_posterior_narrows(self, trained_model, speclib):
        """The latent posterior σ(z) should be smaller with many bands than few.

        Average over multiple spectra since individual samples can be noisy.
        Test extreme comparison (5 vs 200 bands) rather than strict monotonicity.
        """
        rng = np.random.default_rng(66)

        sigma_few, sigma_many = [], []
        for spec in speclib.spectra[:15]:
            refl = np.clip(np.nan_to_num(spec.resample(WL_DENSE)), 0, 1)
            rtm = simplified_toa_radiance(refl, WL_DENSE)

            for n_bands, bucket in [(5, sigma_few), (200, sigma_many)]:
                sensor = sample_virtual_sensor(rng, n_bands_range=(n_bands, n_bands), strategy="regular")
                band_rad = apply_sensor(sensor, WL_DENSE, rtm.toa_radiance).astype(np.float32)
                with torch.no_grad():
                    wl_t = torch.tensor(sensor.center_wavelength_nm).unsqueeze(0)
                    fw_t = torch.tensor(sensor.fwhm_nm).unsqueeze(0)
                    rad_t = torch.tensor(band_rad).unsqueeze(0)
                    _, _, z_logsig = trained_model.encode(wl_t, fw_t, rad_t)
                    bucket.append(z_logsig.exp().mean().item())

        mean_few = np.mean(sigma_few)
        mean_many = np.mean(sigma_many)
        assert mean_few > mean_many, (
            f"Latent posterior did not narrow: 5-band σ={mean_few:.4f}, "
            f"200-band σ={mean_many:.4f}"
        )


