class TestPhysicalConsistency:
    """Model predictions should respect basic physical relationships."""

    def test_brighter_surface_higher_radiance(self, predictor, speclib):
        """A higher-reflectance surface should produce higher predicted radiance
        at most wavelengths (under the same atmosphere)."""
        sensor = SENTINEL2_MSI
        atmos = AtmosphericState(aod_550=0.15)

        # Low-reflectance: water.  High-reflectance: bright soil.
        water_spec = speclib.filter_category("water").spectra[0]
        soil_spec = speclib.filter_category("soils").spectra[0]

        water_rad, _, _ = _make_test_case(water_spec, WL_DENSE, sensor, atmos=atmos)
        soil_rad, _, _ = _make_test_case(soil_spec, WL_DENSE, sensor, atmos=atmos)

        pred_water = predictor.predict(
            wavelength_nm=sensor.center_wavelength_nm,
            fwhm_nm=sensor.fwhm_nm,
            radiance=water_rad,
            query_wavelength_nm=QUERY_WL,
            n_samples=16,
        )
        pred_soil = predictor.predict(
            wavelength_nm=sensor.center_wavelength_nm,
            fwhm_nm=sensor.fwhm_nm,
            radiance=soil_rad,
            query_wavelength_nm=QUERY_WL,
            n_samples=16,
        )

        # Soil should have higher mean predicted radiance across most of the spectrum.
        frac_higher = np.mean(pred_soil.spectral_mean > pred_water.spectral_mean)
        assert frac_higher > 0.7, (
            f"Soil predicted brighter than water at only {frac_higher:.1%} of wavelengths"
        )

    def test_predictions_nonnegative(self, predictor, speclib):
        """Predicted mean radiance should be non-negative (physical constraint)."""
        sensor = AVIRIS_NG
        rng = np.random.default_rng(333)

        for spec in speclib.spectra[:20]:
            band_rad, _, _ = _make_test_case(spec, WL_DENSE, sensor, rng=rng)
            pred = predictor.predict(
                wavelength_nm=sensor.center_wavelength_nm,
                fwhm_nm=sensor.fwhm_nm,
                radiance=band_rad,
                query_wavelength_nm=QUERY_WL,
                n_samples=16,
            )
            frac_negative = np.mean(pred.spectral_mean < 0)
            assert frac_negative < 0.1, (
                f"{frac_negative:.1%} of predicted radiance values are negative"
            )

    def test_sensor_predictions_correlated(self, predictor, speclib):
        """Predictions from different sensors observing the same scene should
        be positively correlated — they see the same underlying spectrum."""
        from scipy.stats import pearsonr

        correlations = []
        for spec in speclib.spectra[:10]:
            atmos = AtmosphericState(aod_550=0.2)
            predictions = {}
            for name, sensor in [("Sentinel-2", SENTINEL2_MSI),
                                  ("AVIRIS-NG", AVIRIS_NG)]:
                band_rad, _, _ = _make_test_case(spec, WL_DENSE, sensor, atmos=atmos)
                pred = predictor.predict(
                    wavelength_nm=sensor.center_wavelength_nm,
                    fwhm_nm=sensor.fwhm_nm,
                    radiance=band_rad,
                    query_wavelength_nm=QUERY_WL,
                    n_samples=32,
                )
                predictions[name] = pred

            corr, _ = pearsonr(
                predictions["AVIRIS-NG"].spectral_mean,
                predictions["Sentinel-2"].spectral_mean,
            )
            correlations.append(corr)

        mean_corr = np.mean(correlations)
        assert mean_corr > 0.5, (
            f"Mean cross-sensor correlation is {mean_corr:.3f} (expected >0.5 — "
            f"predictions from different sensors should agree on spectral shape)"
        )

    @pytest.mark.xfail(reason="Demo model's narrow uncertainty makes this hard to pass")
    def test_sensor_consistency_within_uncertainty(self, predictor, speclib):
        """Aspirational: cross-sensor predictions agree within combined ±3σ
        at >70% of wavelengths.  Requires well-calibrated uncertainty."""
        spec = speclib.spectra[7]
        atmos = AtmosphericState(aod_550=0.2)

        preds = {}
        for name, sensor in [("Sentinel-2", SENTINEL2_MSI), ("AVIRIS-NG", AVIRIS_NG)]:
            band_rad, _, _ = _make_test_case(spec, WL_DENSE, sensor, atmos=atmos)
            preds[name] = predictor.predict(
                wavelength_nm=sensor.center_wavelength_nm,
                fwhm_nm=sensor.fwhm_nm,
                radiance=band_rad,
                query_wavelength_nm=QUERY_WL,
                n_samples=32,
            )

        combined_sigma = np.sqrt(preds["AVIRIS-NG"].spectral_std ** 2 + preds["Sentinel-2"].spectral_std ** 2)
        diff = np.abs(preds["AVIRIS-NG"].spectral_mean - preds["Sentinel-2"].spectral_mean)
        frac_consistent = np.mean(diff < 3 * combined_sigma)
        assert frac_consistent > 0.7, (
            f"Cross-sensor consistency: {frac_consistent:.1%} (target >70%)"
        )
