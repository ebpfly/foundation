"""Evaluation tests for a trained SpectralNP model.

These tests verify that the model has learned something meaningful,
not just that the code runs.  They load a trained checkpoint and
check statistical properties across many test samples.

Run with:
    pytest tests/test_evaluation.py -v

Requires demo_model.pt in the repo root (generated by train_demo.py).
"""

